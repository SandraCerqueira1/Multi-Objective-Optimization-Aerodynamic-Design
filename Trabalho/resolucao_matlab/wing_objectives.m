function f = wing_objectives(x)
% x = [alpha1, alpha2, s, c]

alpha1 = x(1);
alpha2 = x(2);
s      = x(3);
c      = x(4);

% Coeficiente de arrasto 
CD = 0.02 ...
    + 0.01*alpha1 ...
    + 0.012*alpha2 ...
    + 0.15*c ...
    + 0.0005*s ...
    + 0.0008*alpha1*alpha2 ...
    + 0.0003*alpha1^2 ...
    + 0.0004*alpha2^2;

% Coeficiente de downforce 
CL = 1.0 ...
    + 0.08*alpha1 ...
    + 0.06*alpha2 ...
    + 0.5*c ...
    - 0.0003*s ...
    + 0.002*alpha1*alpha2 ...
    - 0.0002*alpha1^2 ...
    - 0.0001*alpha2^2;

% Custo de manufatura 
Cman = 100 ...
     + 15*c^2 ...
     + 0.05*s ...
     + 0.8*(alpha1 - alpha2)^2;

% Vetor de objetivos
f = [CD, -CL, Cman];
end
